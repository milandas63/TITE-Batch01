package com.compute;

public class Mathematics {
	public int add(int x, int y) {
		return x+y;
	}

	public double add(double x, double y) {
		return x+y;
	}

	public int subtract(int x, int y) {
		return x-y;
	}

	public int multiply(int x, int y) {
		return x*y;
	}

	public double divide(int x, int y) {
		return x/y;
	}

}
